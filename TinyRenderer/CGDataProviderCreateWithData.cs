namespace TinyRenderer
{
    internal class CGDataProviderCreateWithData
    {
        private object nULL1;
        private byte[] data;
        private object dataLength;
        private object nULL2;

        public CGDataProviderCreateWithData(object nULL1, byte[] data, object dataLength, object nULL2)
        {
            this.nULL1 = nULL1;
            this.data = data;
            this.dataLength = dataLength;
            this.nULL2 = nULL2;
        }
    }
}